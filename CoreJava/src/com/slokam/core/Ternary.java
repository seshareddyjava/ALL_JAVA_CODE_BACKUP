//Ternary operator example code
package com.slokam.core;

public class Ternary {
	//main method
	public static void main(String[] args) {
	//ternary operaor
		int i = 75;
		//j value assigned based on the first condition,if it is true then 1 will be assigned else 0 will be assigned to j
		int j = (i>=35&&i<=100)?1:0;
		System.out.println(j);

	}

}
