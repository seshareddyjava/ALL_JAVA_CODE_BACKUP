// Switch condition example
package com.slokam.core;

public class SwitchExample {
//main method
	public static void main(String[] args) {
		// take a variable
		char c = 'b';
		//giving variable as input parameter to the switch
		switch('b') {
		case 'a': System.out.println("value is "); break;
		case 'b': System.out.println("value is "); break;
		case 'c': System.out.println("value is "); break;
		case 'd': System.out.println("value is "); break;
		case 'e': System.out.println("value is "); break;
		default:System.out.println("please enter character in between a-e");
		}
	}

}
